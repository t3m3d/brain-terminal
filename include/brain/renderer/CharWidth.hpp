// Terminal column width of a Unicode codepoint: 0 (combining / zero-width),
// 1 (normal), or 2 (East-Asian wide / fullwidth / emoji). Based on the classic
// Markus Kuhn wcwidth ranges plus the common emoji blocks. Not byte-perfect
// against the full UCD, but correct for the cases that actually misalign a
// terminal: CJK text, fullwidth forms, emoji, and combining accents.

